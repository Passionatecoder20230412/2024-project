function Todos() {
  const [todos,dispatch]=useReducer(reducer,initialtodos);
  const handlecomplete=(todo)=>{
     dispatch({type:'complete',id:todo.id})
  }
  return (
    <div>
    {todos.map((todo)=>(
    <div key={todo.id}>
      <label>
        <input type='checkbox' checked={todo.complete} onChange={()=>handlecomplete()}/>
        {todo.title}
      </label>
    </div>
))}
</div>
);
}

export default Todos
