import React ,{useReducer}from 'react'
const initialtodos=[
  {
    id :1,
    title:'todo 1',
    complete: true
  },
  {
    id :2,
    title:'todo 2',
    complete: true
  },
  {
    id :3,
    title:'todo 3',
    complete: false
  }
];
const reducer=(state,action)=>{
  switch(action.type){
    case 'complete':
      return state.map((todo)=>{
        if(todo.id===action.id){
          return {...todo,complete:!todo.complete}
        }
      })
      default : return state;
  }
}
